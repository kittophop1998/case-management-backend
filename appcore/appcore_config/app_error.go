package appcore_config

import (
	"net/http"
)

type AppError struct {
	Code       string
	Message    Message
	HTTPStatus int
	Details    interface{}
}

type Message struct {
	Th string `json:"th"`
	En string `json:"en"`
}

func (e *AppError) Error() string {
	return e.Message.Th
}

func NewAppError(code string, message Message, status int, details interface{}) *AppError {
	return &AppError{
		Code:       code,
		Message:    message,
		HTTPStatus: status,
		Details:    details,
	}
}

